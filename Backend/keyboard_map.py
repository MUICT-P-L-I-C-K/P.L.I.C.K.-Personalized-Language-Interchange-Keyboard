"""
keyboard_map.py — แผนที่ปุ่มกด (Keyboard Layout Mapping)

ไฟล์นี้เก็บแผนที่การแปลงตัวอักษรระหว่าง keyboard layout ภาษาอังกฤษ (QWERTY)
กับภาษาไทย (Kedmanee) เอาไว้ใช้เมื่อผู้ใช้พิมพ์ผิดภาษา เช่น พิมพ์ "สวัสดี"
แต่ลืมสลับภาษาจากอังกฤษ → จะได้ "aw;cofu" ออกมา

ฟังก์ชันหลัก:
  - convert_keyboard()  : แปลงข้อความจาก layout หนึ่งไป layout อีกภาษา
  - normalize_quotes()  : ทำให้เครื่องหมายอัญประกาศทุกแบบเป็นมาตรฐาน
  - is_mostly_thai()    : ตรวจว่าข้อความมีตัวอักษรไทยหรือไม่
"""

import re


# =============================================================================
# Keyboard Mapping Tables
# =============================================================================

# แปลง key จาก English → Thai (ปุ่มเดียวกันบน keyboard แต่ layout ต่างกัน)
ENG_TO_THAI = {
    # ─── แถวตัวเลข (Number row) ───
    "`": "_", "1": "ๅ", "2": "/", "3": "-", "4": "ภ", "5": "ถ",
    "6": "ุ", "7": "ึ", "8": "ค", "9": "ต", "0": "จ",
    "-": "ข", "=": "ช",
    # ─── แถวตัวเลข + Shift ───
    "~": "%", "!": "+", "@": "๑", "#": "๒", "$": "๓", "%": "๔",
    "^": "ู", "&": "฿", "*": "๕", "(": "๖", ")": "๗",
    "_": "๘", "+": "๙",

    # ─── แถวบน (QWERTY) ───
    "q": "ๆ", "w": "ไ", "e": "ำ", "r": "พ", "t": "ะ",
    "y": "ั", "u": "ี", "i": "ร", "o": "น", "p": "ย",
    "[": "บ", "]": "ล", "\\": "ฃ",
    # ─── แถวบน + Shift ───
    "Q": "๐", "W": '"', "E": "ฎ", "R": "ฑ", "T": "ธ",
    "Y": "ํ", "U": "๊", "I": "ณ", "O": "ฯ", "P": "ญ",
    "{": "ฐ", "}": ",", "|": "ฅ",

    # ─── แถวกลาง (ASDF / Home row) ───
    "a": "ฟ", "s": "ห", "d": "ก", "f": "ด", "g": "เ",
    "h": "้", "j": "่", "k": "า", "l": "ส", ";": "ว", "'": "ง",
    # ─── แถวกลาง + Shift ───
    "A": "ฤ", "S": "ฆ", "D": "ฏ", "F": "โ", "G": "ฌ",
    "H": "็", "J": "๋", "K": "ษ", "L": "ศ", ":": "ซ", '"': ".",

    # ─── แถวล่าง (ZXCV) ───
    "z": "ผ", "x": "ป", "c": "แ", "v": "อ", "b": "ิ",
    "n": "ื", "m": "ท", ",": "ม", ".": "ใ", "/": "ฝ",
    # ─── แถวล่าง + Shift ───
    "Z": "(", "X": ")", "C": "ฉ", "V": "ฮ", "B": "ฺ",
    "N": "์", "M": "?", "<": "ฒ", ">": "ฬ", "?": "ฦ",
}

# สร้าง reverse map: Thai → English (กลับด้านจากตารางข้างบน)
THAI_TO_ENG = {thai: eng for eng, thai in ENG_TO_THAI.items()}


# =============================================================================
# Text Utilities
# =============================================================================

def normalize_quotes(text: str) -> str:
    """
    แปลงเครื่องหมายอัญประกาศแบบพิเศษ (smart quotes, fancy quotes)
    ให้เป็นเครื่องหมายมาตรฐาน ASCII (' และ ")
    
    ทำไมต้องทำ: เพราะบางเว็บไซต์หรือ OS จะแปลง ' เป็น ' หรือ " เป็น "
    ซึ่งทำให้ mapping table หาไม่เจอ
    """
    if not text:
        return text
    text = re.sub(r"[''‛❛❜＇]", "'", text)
    text = re.sub(r'[""‟❝❞〝〞＂]', '"', text)
    return text


def is_mostly_thai(text: str) -> bool:
    """
    ตรวจว่าข้อความมีตัวอักษรไทย (Unicode range 0E00-0E7F) หรือไม่
    ใช้เพื่อตัดสินว่าผู้ใช้กำลังพิมพ์ภาษาอะไร
    """
    if not text:
        return False
    thai_count = len(re.findall(r'[\u0E00-\u0E7F]', text))
    return thai_count > 0


def convert_keyboard(text: str, to_lang: str) -> str:
    """
    แปลงข้อความเสมือนว่าพิมพ์ผิด layout
    
    ตัวอย่าง:
      convert_keyboard("สวัสดี", "en")  → แปลง Thai→English
      convert_keyboard("hello", "th")   → แปลง English→Thai
    
    Args:
        text:    ข้อความต้นฉบับ
        to_lang: ภาษาเป้าหมาย ("en" หรือ "th")
    """
    text = normalize_quotes(text)
    if to_lang == "en":
        return ''.join(THAI_TO_ENG.get(ch, ch) for ch in text)
    else:
        return ''.join(
            ENG_TO_THAI.get(ch, ENG_TO_THAI.get(ch.lower(), ch))
            for ch in text
        )
